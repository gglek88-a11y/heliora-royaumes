create table if not exists public.heliora_players (
  player_id text primary key,
  user_id uuid unique references auth.users(id) on delete cascade,
  name text not null default 'Commandant',
  guild_tag text not null default 'HDH',
  guild jsonb not null default '{}'::jsonb,
  kingdom_power integer not null default 0,
  resources jsonb not null default '{}'::jsonb,
  save_state jsonb not null default '{}'::jsonb,
  save_version integer not null default 2,
  synced_at timestamptz not null default now()
);

alter table public.heliora_players
  add column if not exists user_id uuid unique references auth.users(id) on delete cascade;

alter table public.heliora_players
  add column if not exists save_version integer not null default 2;

create table if not exists public.heliora_chat (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete set null,
  from_name text not null default 'Alliance',
  message text not null,
  kind text not null default '',
  created_at timestamptz not null default now()
);

alter table public.heliora_chat
  add column if not exists user_id uuid references auth.users(id) on delete set null;

create table if not exists public.heliora_guilds (
  id uuid primary key default gen_random_uuid(),
  owner_user_id uuid not null references auth.users(id) on delete cascade,
  name text not null,
  tag text not null unique,
  description text not null default '',
  power integer not null default 0,
  score integer not null default 0,
  member_count integer not null default 1,
  is_open boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.heliora_guild_members (
  guild_id uuid not null references public.heliora_guilds(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  player_id text not null,
  name text not null default 'Commandant',
  role text not null default 'member' check (role in ('leader', 'officer', 'member', 'recruit')),
  kingdom_power integer not null default 0,
  contribution integer not null default 0,
  joined_at timestamptz not null default now(),
  primary key (guild_id, user_id)
);

create table if not exists public.heliora_guild_invites (
  id uuid primary key default gen_random_uuid(),
  guild_id uuid not null references public.heliora_guilds(id) on delete cascade,
  invited_email text not null,
  invited_user_id uuid references auth.users(id) on delete cascade,
  created_by uuid not null references auth.users(id) on delete cascade,
  status text not null default 'pending' check (status in ('pending', 'accepted', 'declined', 'cancelled')),
  created_at timestamptz not null default now()
);

create index if not exists heliora_guild_members_user_idx
on public.heliora_guild_members (user_id);

create index if not exists heliora_guild_invites_email_idx
on public.heliora_guild_invites (invited_email, status);

create or replace view public.heliora_leaderboard as
select
  player_id,
  name,
  guild_tag,
  guild,
  kingdom_power,
  synced_at
from public.heliora_players
where user_id is not null;

create or replace view public.heliora_guild_leaderboard as
select
  id,
  name,
  tag,
  score,
  power,
  member_count,
  is_open,
  updated_at
from public.heliora_guilds;

create or replace function public.heliora_is_guild_manager(target_guild uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.heliora_guild_members
    where guild_id = target_guild
      and user_id = auth.uid()
      and role in ('leader', 'officer')
  );
$$;

