create policy "heliora guilds manager update"
on public.heliora_guilds
for update
to authenticated
using (public.heliora_is_guild_manager(id) or owner_user_id = auth.uid())
with check (public.heliora_is_guild_manager(id) or owner_user_id = auth.uid());

create policy "heliora guild members authenticated read"
on public.heliora_guild_members
for select
to authenticated
using (true);

create policy "heliora guild members join"
on public.heliora_guild_members
for insert
to authenticated
with check (
  user_id = auth.uid()
  and (
    exists (
      select 1
      from public.heliora_guilds
      where id = guild_id
        and (is_open = true or owner_user_id = auth.uid())
    )
  )
);

create policy "heliora guild members self or manager update"
on public.heliora_guild_members
for update
to authenticated
using (user_id = auth.uid() or public.heliora_is_guild_manager(guild_id))
with check (user_id = auth.uid() or public.heliora_is_guild_manager(guild_id));

create policy "heliora guild members self or manager delete"
on public.heliora_guild_members
for delete
to authenticated
using (user_id = auth.uid() or public.heliora_is_guild_manager(guild_id));

create policy "heliora guild invites related read"
on public.heliora_guild_invites
for select
to authenticated
using (
  public.heliora_is_guild_manager(guild_id)
  or invited_user_id = auth.uid()
  or lower(invited_email) = lower(auth.jwt() ->> 'email')
);

create policy "heliora guild invites manager insert"
on public.heliora_guild_invites
for insert
to authenticated
with check (
  created_by = auth.uid()
  and public.heliora_is_guild_manager(guild_id)
);

create policy "heliora guild invites related update"
on public.heliora_guild_invites
for update
to authenticated
using (
  public.heliora_is_guild_manager(guild_id)
  or invited_user_id = auth.uid()
  or lower(invited_email) = lower(auth.jwt() ->> 'email')
)
with check (
  public.heliora_is_guild_manager(guild_id)
  or invited_user_id = auth.uid()
  or lower(invited_email) = lower(auth.jwt() ->> 'email')
);

